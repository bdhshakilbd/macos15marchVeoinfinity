/// Localization service — call L.of(context).t('key') or L.tr('key')
class LocalizationService extends ChangeNotifier {
  static final LocalizationService _instance = LocalizationService._internal();
  factory LocalizationService() => _instance;
  LocalizationService._internal();

  AppLanguage _language = AppLanguage.english;
  AppLanguage get language => _language;

  static const String _prefKey = 'app_language';

  Future<void> load() async {
    try {
      final prefs = await SharedPreferences.getInstance();
      final saved = prefs.getString(_prefKey);
      if (saved != null) {
        final found = AppLanguage.values.firstWhere(
          (l) => l.code == saved,
          orElse: () => AppLanguage.english,
        );
        _language = found;
      }
    } catch (_) {}
  }

  Future<void> setLanguage(AppLanguage lang) async {
    if (_language == lang) return;
    _language = lang;
    notifyListeners();
    try {
      final prefs = await SharedPreferences.getInstance();
      await prefs.setString(_prefKey, lang.code);
    } catch (_) {}
  }

  /// Translate a key
  String tr(String key) {
    final map = _translations[_language.code] ?? _translations['en']!;
    return map[key] ?? _translations['en']![key] ?? key;
  }

